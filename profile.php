  <h3 style="margin-top: 0; margin-bottom: 0">&nbsp;</h3>
<h3 style="margin-top: 0; margin-bottom: 0"><b><font face="Verdana" size="2">LATAR BELAKANG 
(<i><font color="#008000">BACKGROUND</font></i>)</font></b></h3><b>
  </b>
  <p align="justify" style="margin-top: 0; margin-bottom: 0">&nbsp;</p><p align="justify" style="margin-top: 0; margin-bottom: 0">
<font face="Verdana" size="2">Di dalam Garis-Garis Besar
  Haluan Negara, salah satu usaha di dalamnya adalah peningkatan penyediaan air
  bersih. Selanjutnya sesuai dengan program yang dicanangkan mengenai Ilmu
  Pengetahuan, Teknologi dan Penelitian, Garis-Garis Besar Haluan Negara juga
  memberikan pokok-pokok pengarahannya sebagai berikut: Ilmu pengetahuan dan
  teknologi memegang peranan penting serta mempengaruhi perkembangan di segala
  bidang kehidupan dan pembangunan. Oleh karena itu pengembangan dan
  penguasaannya perlu dilanjutkan dan diarahkan untuk memajukan kecerdasan dan
  kemampuan bangsa serta kesejahtraan seluruh masyarakat dalam rangka
  meningkatkan kualitas hidupnya. Pengembangan dan penerapan teknologi
  disesuaikan dengan prioritas pembangunan dan diarahkan kepada pemilihan
  teknologi tepat yang dapat meningkatkan kemampuan dan produktivitas nasional,
  nilai tambah, pertumbuhan ekonomi, perluasan lapangan kerja, pemerataan
  hasil-hasil pembangunan dan penggunaan alat-alat produksi dalam negeri.
  Penerapan teknologi ditujukan untuk meningkatkan efisiensi dan produktivitas
  serta daya saing di tingkat internasional dan mempersiapkan landasan yang
  lebih kokoh bagi tahap-tahap pembangunan selanjutnya. Pemilihan, penerapan dan
  pengembangan teknologi memperhatikan nilai-nilai budaya bangsa dan kondisi
  sosial budaya yang mendukungnya serta kelestarian lingkungan hidup. </font>
  <font color="#008000">
      <span lang="EN-AU" style="font-size: 10.0pt; font-family: Verdana; font-style:italic">
<br>
<br>
One of 
      the main program in The State Policy Guidelines is to improve clean water 
      supply system. Then, based on program related to Science, Technological 
      and Research Program, the guidelines&nbsp; also give its direction, as follows 
      : Science and Technology play important role and also influence the whole 
      ascpects of &nbsp;life and development. Therefore, the development and its 
      competency needs to be continued and directed to improve intelligence and 
      ability of the nation and also welfare of the society in order to improve 
      their quality of life. Development and application of technology are 
      adjusted according to its priority,and are directed to selection of the 
      right technology which will improve the capability and national 
      productivity, added value, economic growth, increase of employment, 
      &nbsp;distribution of prosperity, and the usage &nbsp;of &nbsp;domestic production 
      appliances. The 
      purpose of&nbsp; the technology application &nbsp;is to improve the &nbsp;efficiency and 
      productivity as well as competitive advantage in international level, and 
      to prepare stronger foundation for the next development steps. Selection, 
      application and development of technology&nbsp; have to pay attention&nbsp; to 
      nation cultural values, social-cultural condition of the nation and 
      environmental conservation.</span></font></p><p align="justify" style="margin-top: 0; margin-bottom: 0">&nbsp;</p><p align="justify" style="margin-top: 0; margin-bottom: 0">
<font face="Verdana" size="2">Untuk mengisi tujuan nasional
  tersebut Kelompok Teknologi Pengelolaan Air Bersih dan Limbah Cair, Direktorat
  Lingkungan, Deputi Bidang Teknologi Informasi, Energi, Material dan
  Lingkungan, mempunyai tugas pokok melakukan pengkajian dan penerapan teknologi
  dalam bidang pengolahan air baik dalam skala perkotaan maupun pedesaan. </font>
  <font color="#008000">
      <span lang="EN-AU" style="font-size: 10.0pt; font-family: Verdana; font-style:italic">
<br>
<br>
In 
      order to achieve &nbsp;the National objective,&nbsp; Group of Clean Water and 
      Wastewater Treatment Technology, Environmental Directorate, Deputy of the 
      Information, Energy, Material and Environmental Technologies, has a main 
      task &nbsp;to do assessment and application of technology on water treatment in 
      rural and urban areas.</span></font></p><p align="justify" style="margin-top: 0; margin-bottom: 0">&nbsp;</p><p align="justify" style="margin-top: 0; margin-bottom: 0">
<font face="Verdana" size="2">Pengkajian dilakukan terhadap
  lima aspek yaitu aspek teknis, ekonomi, sosial dan budaya, pengelolaan, dan
  lingkungan yang nantinya diharapkan akan mendapatkan hasil kajian yang berupa
  perangkat keras maupun perangkat lunak yang layak untuk diterapkan untuk
  peningkatan mutu dan pemenuhan kebutuhan air bersih serta pelestarian
  lingkungan bagi masyarakat. </font>
  <font color="#008000">
      <span lang="EN-AU" style="font-size: 10.0pt; font-family: Verdana; font-style:italic">
<br>
<br>
      Assessment process &nbsp;is carried out on five aspects, i.e. technical, 
      economy, cultural and social, management, and environment aspects which 
      are expected to produce competent hardware and software to be applied to 
      improve &nbsp;clean water supply quality and environmental conservation for 
      public services.</span></font></p><p align="justify" style="margin-top: 0; margin-bottom: 0">&nbsp;</p><p align="justify" style="margin-top: 0; margin-bottom: 0">
<font face="Verdana" size="2">Saat ini tingkat pelayanan
  air bersih untuk masyarakat perkotaan baru mencapai 40 % sedangkan untuk
  masyarakat di pedesaan baru mencapai sekitar 35 %. Rendahnya tingkat pelayanan
  tersebut disebabkan oleh banyak hal antara lain langkanya sumber-sumber air
  yang potensial (kurangnya kuantitas air baku), kualitas air baku yang belum
  memenuhi syarat kesehatan, belum dimanfaatkannya sumber air secara optimal,
  kurangnya sarana pengolahan air, kurangnya penyebaran informasi teknologi
  pengolahan air baik yang sederhana maupun yang sudah menggunakan teknologi
  tinggi, kehidupan sosial dan budaya yang sulit menerima kehadiran teknologi
  baru dan biaya pembangunan sarana pengolahan air dan perawatannya yang tidak
  terjangkau oleh masyarakat. Penurunan kualitas air baku banyak diakibatkan
  oleh adanya pencemaran air limbah rumah tangga atau industri yang melebihi
  daya dukung lingkungannya. </font>
  <font color="#008000">
      <span lang="EN-AU" style="font-size: 10.0pt; font-family: Verdana; font-style:italic">
<br>
<br>
Public 
      water supply for urban society now has reached at about 40% of population, 
      while in rural society has reached around 35%. The decrement of service 
      level may be caused by : lack of potential raw water resources, low of raw 
      water quantity, low of raw healthy water quality standard, raw water 
      resoursces has not been exploited optimally, lack of water treatment 
      equipment, lack of water treatment technology information either the 
      simple or the high technology, culture&nbsp; and social life which are not easy 
      to accept new technologies and high cost of development and maintenance of 
      water treatment facilities. Degradation of raw water quality is mostly 
      resulted from domestic or industrial wastes contamination which exceeds 
      the environmental supporting limit.</span></font></p><p align="justify" style="margin-top: 0; margin-bottom: 0">&nbsp;</p><p align="justify" style="margin-top: 0; margin-bottom: 0">
<font face="Verdana" size="2">Untuk mengatasi masalah
  tersebut perlu dilakukan pengkajian terhadap sistem penyediaan air bersih
  didaerah yang mempunyai masalah dengan pendekatan ke lima aspek seperti
  tersebut di atas untuk mendapatkan pemecahan masalah yang optimal sehingga
  hasilnya dapat dirasakan secara merata oleh masyarakat baik yang tinggal di
  perkotaan maupun di pedesaan. </font>
  </p><p align="justify" style="margin-top: 0; margin-bottom: 0">&nbsp;</p><p align="justify" style="margin-top: 0; margin-bottom: 0">
<font color="#008000">
<span lang="EN-AU" style="font-size: 10.0pt; font-family: Verdana; font-style:italic">To 
      overcome the problem it is needed to do assessment to the clean water 
      supply system in areas which have such problems with &nbsp;approach to five 
      aspects mentioned above to solve the problem optimally so that the result 
      is enjoyed by &nbsp;urban and rural society</span></font></p><p align="justify" style="margin-top: 0; margin-bottom: 0">&nbsp;</p></a><p align="left" style="margin-top: 0; margin-bottom: 0"><a name="latar"></a><a href="#top"><font face="Verdana" size="2">Kembali ke
